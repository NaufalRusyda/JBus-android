package com.naufalRusydaJBusRD.jbus_android.model;

/**
 * The Price class represents the pricing information associated with a bus in the JBus Android application.
 *
 * @author Naufal Rusyda Santosa
 * @version 1.0
 */
public class Price {

    /**
     * The rebate amount applied to the bus price.
     */
    public double rebate;

    /**
     * The base price of the bus.
     */
    public double price;
}
